#!/usr/bin/env python3
"""A function that concatenates two arrays"""


def cat_arrays(arr1, arr2):
    """
    Concatenate two arrays
    Parameters:
        arr1 (list): a list of integers/floats
        arr2 (list): a list of integers/floats
    Returns:
        list: a new list of integers/floats
    """
    return arr1 + arr2